package com.miet.pp.lab4;

public class Result {
	private double p;
	private double v;
	private double a;
	
	public Result(double _p, double _v, double _a)
	{
		p = _p;
		v = _v;
		a = _a;
	}
	
	public double getP() {
		return p;
	}
	
	public double getV() {
		return v;
	}
	
	public double getA() {
		return a;
	}
}
